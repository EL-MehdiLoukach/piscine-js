
function bloodySunday(date){

    if (date.getDate() === 2 ){
        return "Tuesday"
    }

    if (date.getDate() === 3 ){
        return "Tuesday"
    }

    if (date.getDate() === 4 ){
        return "Wednesday"
    }

    if (date.getDate() === 5 ){
        return "Thursday"
    }
    if (date.getDate() === 6 ){
        return "Friday"
    }

    return "Monday"
}


